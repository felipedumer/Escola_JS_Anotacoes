let spaceship = "Goli"
let velocity = 80

console.log(velocity == 90) // return false
console.log(spaceship == "Goli") // return true

console.log(velocity == 80) // return true

console.log(velocity === "80") // comparar tipo de dado também

// diferença

console.log(velocity != 80) // return false
console.log(velocity != 70) // return true

console.log(velocity != "80") // return false

console.log(velocity !== "80") // compara o tipo de dado

// maior

console.log(velocity > 70) // return true
console.log(spaceship > "A") // return true primeira letra maior

console.log(velocity >= 80) // return true
console.log(spaceship >= "Goli") // return true

// menor

console.log(velocity < 70) // return false
console.log(spaceship < "A") // return false primeira letra maior

console.log(velocity <= 80) // return true
console.log(spaceship <= "Goli") // return true

